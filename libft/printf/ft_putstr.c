#include "ft_printf.h"

void	ft_putstr(char *a, int *inft_counter)
{
	size_t	i;

	i = 0;
	if (!a)
	{
		a = "(null)";
	}
	while (a[i])
	{
		write(1, &a[i], 1);
		i++;
		(*inft_counter)++;
	}
}
